TARGET_BYTE = 2
CORRECT_KEY_BYTE = 0xe0


@dataclass
class DataIngestionConfig:
    raw_data_path: str = os.path.join("artifacts", "raw", "ASCAD_processed.h5")
    profiling_traces_path: str = os.path.join("artifacts", "ingested", "X_prof.npy")
    profiling_labels_path: str = os.path.join("artifacts", "ingested", "y_prof.npy")
    profiling_plaintexts_path: str = os.path.join("artifacts", "ingested", "pt_prof.npy")
    attack_traces_path: str = os.path.join("artifacts", "ingested", "X_atk.npy")
    attack_labels_path: str = os.path.join("artifacts", "ingested", "y_atk.npy")
    attack_plaintexts_path: str = os.path.join("artifacts", "ingested", "pt_atk.npy")


class DataIngestion:
    def __init__(self):
        self.config = DataIngestionConfig()

    def _generate_hw_labels(self, plaintexts: np.ndarray) -> np.ndarray:
        """Generate Hamming Weight labels from plaintext array."""
        return np.array([
            bin(AES_SBOX[int(pt[TARGET_BYTE]) ^ CORRECT_KEY_BYTE]).count('1')
            for pt in plaintexts
        ], dtype=np.int32)

    def initiate_data_ingestion(self):
        """
        Load ASCAD_processed.h5, generate HW labels, save arrays to artifacts/ingested/.
        Returns (X_prof, y_prof, pt_prof, X_atk, y_atk, pt_atk).
        """
        logging.info("Data ingestion started")
        try:
            if not os.path.exists(self.config.raw_data_path):
                raise FileNotFoundError(
                    f"ASCAD_processed.h5 not found at {self.config.raw_data_path}. "
                    "Run generate_ascad.py first."
                )

            logging.info(f"Loading HDF5 file: {self.config.raw_data_path}")
            with h5py.File(self.config.raw_data_path, "r") as f:
                X_prof  = f["Profiling_traces/traces"][:].astype(np.float32)
                pt_prof = f["Profiling_traces/metadata/plaintext"][:]
                X_atk   = f["Attack_traces/traces"][:].astype(np.float32)
                pt_atk  = f["Attack_traces/metadata/plaintext"][:]

            logging.info(
                f"Loaded — profiling: {X_prof.shape}, attack: {X_atk.shape}"
            )

            y_prof = self._generate_hw_labels(pt_prof)
            y_atk  = self._generate_hw_labels(pt_atk)

            logging.info(
                f"Labels generated — profiling classes: {np.unique(y_prof)}, "
                f"attack classes: {np.unique(y_atk)}"
            )

            os.makedirs(os.path.dirname(self.config.profiling_traces_path), exist_ok=True)
            np.save(self.config.profiling_traces_path,    X_prof)
            np.save(self.config.profiling_labels_path,    y_prof)
            np.save(self.config.profiling_plaintexts_path, pt_prof)
            np.save(self.config.attack_traces_path,       X_atk)
            np.save(self.config.attack_labels_path,       y_atk)
            np.save(self.config.attack_plaintexts_path,   pt_atk)

            logging.info("Data ingestion complete — arrays saved to artifacts/ingested/")
            return X_prof, y_prof, pt_prof, X_atk, y_atk, pt_atk

        except Exception as e:
            raise CustomException(e, sys)


